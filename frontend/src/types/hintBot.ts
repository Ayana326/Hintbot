export type HintInstructionTypes = keyof typeof HintInstructions;

export function ConstructHintInstructionForSpecificProblem(
  problem: string,
  hint_type: HintInstructionTypes,
): string {
  return `${HintInstructions[hint_type]}

###問題###
${problem}
`;
}
